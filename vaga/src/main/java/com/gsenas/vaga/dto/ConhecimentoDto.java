package com.gsenas.vaga.dto;

public class ConhecimentoDto {
	private Long id;
	
	private String nome;
	
	public ConhecimentoDto() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

}
